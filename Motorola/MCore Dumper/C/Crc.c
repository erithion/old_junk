#include "crc.h"

static unsigned int _crcValue;

/************************************************************************/
/* Resets the CRC                                                       */
/************************************************************************/
void crcReset()
{
	_crcValue = 0;
}
/************************************************************************/
/* Updates the CRC                                                      */
/************************************************************************/
void crcUpdate(unsigned char value)
{
	_crcValue += value;
}
/************************************************************************/
/* Gets the CRC                                                         */
/************************************************************************/
unsigned char crcGet()
{
	return ~_crcValue;
}